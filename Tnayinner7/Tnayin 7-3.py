# 7-3 .Balance
# Write a program to check if two strings are balanced. For example, strings s1 and
# s2 are balanced if all the characters in the s1 are present in s2. The character’s
# position doesn’t matter.


s1 = "t"
s2 = "Python"

result = True

for i in s1 :
        if i not in s2 :
                result = False
print(result)

